function input.checkcellpress(x1, x2, y1, y2, line, iline, cell)
    
	local truex1 = gridx + x1
	local truex2 = gridx + x2
	local truey1 = gridy + y1
	local truey2 = gridy + y2
	
    local hotfix = false

	local mousex, mousey = love.mouse.getPosition()

  if mousex >= truex1 and mousex <= truex2 and mousey >= truey1 and mousey <= truey2 then

  	if currentRow == row0 and currentCell == 0 then currentRow = line currentCell = cell end
  	if currentRow ~= line and currentCell ~= cell then hotfix = true end

  	cellhoverx = line
  	cellhovery = cell
    
    if canMark == true then
	  if markMode == "O" then
		
	    if iline[cell] == "O" then
		    if line[cell] == "." then
              currentRow = line
	    	  currentCell = cell

	          line[cell] = "O"
			  sfx.mark:play()
		      checkcompleted()
		    elseif line[cell] == "X" then
              currentRow = line
	    	  currentCell = cell

	          line[cell] = "."
			  sfx.erase:play()
	        end
	    elseif iline[cell] == "." then
		  
		  if line[cell] == "." then
            currentRow = line
	    	currentCell = cell

		  	canMark = false
		    mistake.pop()
		    mistake.x = x1 - 9
		    mistake.y = y1 - 7
		  
	        line[cell] = "X"
			sfx.mistake:play()
		  elseif line[cell] == "X" then
            currentRow = line
	    	currentCell = cell

		  	canMark = false
	        line[cell] = "."
			sfx.erase:play()
	      end
		end
		
	  elseif markMode == "X" then

	    if line[cell] == "X" then
	      if currentRow ~= line or currentCell ~= cell or hotfix == true then
	      	if Xmode ~= nil and Xmode == "erase" then
			    currentRow = line
	    	    currentCell = cell

	            line[cell] = "."
			    sfx.erase:play()
			elseif Xmode == nil then
				Xmode = "erase"
                currentRow = line
	    	    currentCell = cell

	            line[cell] = "."
			    sfx.erase:play()
			end
		  elseif currentRow == line and currentCell == cell and Xmode == nil then
		  	  Xmode = "erase"
		  	  currentRow = line
	    	  currentCell = cell
	          line[cell] = "."
			  sfx.erase:play()
		  end			
		elseif line[cell] == "." then
		  if currentRow ~= line or currentCell ~= cell or hotfix == true and Xmode == "mark" then
	      	if Xmode ~= nil and Xmode == "mark" then
			    currentRow = line
	    	    currentCell = cell

	            line[cell] = "X"
			    sfx.Xmark:play()
			elseif Xmode == nil then
				Xmode = "mark"
                currentRow = line
	    	    currentCell = cell

	            line[cell] = "X"
			    sfx.Xmark:play()
			end
		  elseif currentRow == line and currentCell == cell and Xmode == nil then
		  	  Xmode = "mark"
		  	  currentRow = line
	    	  currentCell = cell
	          line[cell] = "X"
			  sfx.Xmark:play()
		  end	
		end
	  end
	end

	updateGridStatus()
  end
end

